# Using trunc

from math import trunc

print(trunc(12.2))
print(trunc(12.5))
print(trunc(12.7))

print(int(12.2))
print(int(12.5))
print(int(12.7))

# Using Floor

from math import floor

print(floor(12.2))
print(floor(12.5))
print(floor(12.7))

from math import floor, trunc

print(floor(-12.7))
print(trunc(-12.7))

# Using Ceil

from math import ceil


print(ceil(12.7))

from math import ceil

print(ceil(-12.7))
